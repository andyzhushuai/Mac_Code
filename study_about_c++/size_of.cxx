# include <iostream>

using namespace std;

int main()
  {
   //print out the size of "int"
   cout <<"int = " <<sizeof(int) <<endl;
   //print out the size of "long int"
   cout <<"long int = " <<sizeof(long int) <<endl;
   //print out the size of "short int"
   cout <<"short int = " <<sizeof(short int) <<endl;
   //print out the size of "char"
   cout <<"char = " <<sizeof(char) <<endl;
   //print out the size of "bool"
   cout <<"bool = " <<sizeof(bool) <<endl;
   //print out the size of "float"
   cout <<"float = " <<sizeof(float) <<endl;
   //print out the size of "double"
   cout <<"double = " <<sizeof(double) <<endl;
   //print out the size of "long double"
   cout <<"long double = " <<sizeof(long double) <<endl;
   //print out the size of "const"
   cout <<"3.14 = " <<sizeof(3.14) <<endl;
   return 0;
  }
